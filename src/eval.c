int evaluation(const Position* pos) {
    int eval_score = 0;

    for (int sq = 0; sq < 64; sq++) {
        int piece = get_piece_on_square(pos, sq);
        printf("Piece: %d\n", piece);

        if (piece == -1) continue;
        int piece_value = piece_values[piece % 6];
        eval_score += (piece < 6) ? piece_value : -piece_value;
    }
    printf("Evaluation: %d\n", eval_score);
    return eval_score;
}